// Declare uma variável contendo um valor de temperatura em graus Celsius e
// informe se está frio (menor que 20) ou quente (20 ou mais).

public class Q4 {
    void main () {
        int graus = 19;
        if (graus < 20) {
            System.out.println(graus + " graus Celsius! " + "Está frio!");
        } else
            System.out.println(graus + " graus Celsius! " + "Está quente!");

    }
}
